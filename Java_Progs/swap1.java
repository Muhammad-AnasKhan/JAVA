public class swap1 {
  public static void main(String[] args) {
    //      swapping var values using 3rd variable
      System.out.println("swapping values  using 3rd variable");
      System.out.println(" ");
      int a=10;
      int b=20;
      int t;
      System.out.println("Values before swap");
      System.out.println("a= "+a);
      System.out.print("b= "+b);
      t=a;
      a=b;
      b=t;
      System.out.println("Values After swap");
      System.out.println("a= "+a);
      System.out.print("b= "+b);

    }
  }
     