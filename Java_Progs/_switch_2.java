public class _switch_2
{
  public static void main(String[] args)
  {
    //      Switch in java
      int a=500,b=20;
     char operator='/';
     switch (operator)
     {
         case '+':
         System.out.print("a+b= "+ (a+b));
         break;
         case '-':
             System.out.print("a-b= "+ (a-b));
         break;
         case '/':
             System.out.print("a/b= "+ (a/b));
         break;
         case '%':
             System.out.print("a%b= "+ (a%b));
         break;


     }
    }
  }
     